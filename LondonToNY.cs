//This namespace holds Strategies in this folder and is required. Do not change it. 
namespace NinjaTrader.NinjaScript.Strategies
{
	public class LondonToNY : Strategy
	{
		private double OpenLONDON;
		private double Zero;
		private double Delta;


		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Enter the description for your new custom Strategy here.";
				Name										= "LondonToNY";
				Calculate									= Calculate.OnPriceChange;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlatSynchronizeAccount;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.ByStrategyPosition;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				W					= 3000;
				S					= 2000;
				OpenLONDON					= 1;
				Zero					= 0;
				Delta					= 1;
			}
			else if (State == State.Configure)
			{
				SetProfitTarget(CalculationMode.Currency, W);
				SetStopLoss(CalculationMode.Currency, S);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1)
				return;

			 // Set 1
			if (Times[0][0].TimeOfDay == new TimeSpan(9, 0, 0))
			{
				OpenLONDON = Close[0];
			}
			
			 // Set 2
			if (Times[0][0].TimeOfDay == new TimeSpan(13, 0, 0))
			{
				Delta = (Close[0] - (OpenLONDON)) ;
			}
			
			 // Set 3
			if ((Times[0][0].TimeOfDay == new TimeSpan(15, 30, 0))
				 && (Delta > Zero))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), @"LONG NY");
			}
			
			 // Set 4
			if ((Times[0][0].TimeOfDay == new TimeSpan(15, 30, 0))
				 && (Delta < Zero))
			{
				EnterShort(Convert.ToInt32(DefaultQuantity), @"SHORT NY");
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="W", Order=1, GroupName="Parameters")]
		public int W
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="S", Order=2, GroupName="Parameters")]
		public int S
		{ get; set; }
		#endregion

	}
}

